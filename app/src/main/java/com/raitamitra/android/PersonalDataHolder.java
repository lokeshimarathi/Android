package com.raitamitra.android;

public class PersonalDataHolder {
    String Phone, Name, DateOfBirth, Gender, Address, AadharNo, PanNo, KisanNo, Land, Feed, Income, ProfileImage, AadharCard;

    public PersonalDataHolder(String phone, String name, String dateOfBirth, String gender, String address, String aadharNo, String panNo, String kisanNo, String land, String feed, String income, String profileImage, String aadharCard) {
        Phone = phone;
        Name = name;
        DateOfBirth = dateOfBirth;
        Gender = gender;
        Address = address;
        AadharNo = aadharNo;
        PanNo = panNo;
        KisanNo = kisanNo;
        Land = land;
        Feed = feed;
        Income = income;
        ProfileImage = profileImage;
        AadharCard = aadharCard;
    }

    public String getPhone() {
        return Phone;
    }

    public void setPhone(String phone) {
        Phone = phone;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getDateOfBirth() {
        return DateOfBirth;
    }

    public void setDateOfBirth(String dateOfBirth) {
        DateOfBirth = dateOfBirth;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String gender) {
        Gender = gender;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getAadharNo() {
        return AadharNo;
    }

    public void setAadharNo(String aadharNo) {
        AadharNo = aadharNo;
    }

    public String getPanNo() {
        return PanNo;
    }

    public void setPanNo(String panNo) {
        PanNo = panNo;
    }

    public String getKisanNo() {
        return KisanNo;
    }

    public void setKisanNo(String kisanNo) {
        KisanNo = kisanNo;
    }

    public String getLand() {
        return Land;
    }

    public void setLand(String land) {
        Land = land;
    }

    public String getFeed() {
        return Feed;
    }

    public void setFeed(String feed) {
        Feed = feed;
    }

    public String getIncome() {
        return Income;
    }

    public void setIncome(String income) {
        Income = income;
    }

    public String getProfileImage() {
        return ProfileImage;
    }

    public void setProfileImage(String profileImage) {
        ProfileImage = profileImage;
    }

    public String getAadharCard() {
        return AadharCard;
    }

    public void setAadharCard(String aadharCard) {
        AadharCard = aadharCard;
    }

}
